function [Y_DCT, Cb_DCT, Cr_DCT] = invSTEP3(Y_Q, Cb_Q, Cr_Q)
    Y_DCT = invQuanY(Y_Q);
    Cb_DCT = invQuanC(Cb_Q);
    Cr_DCT = invQuanC(Cr_Q);
end


